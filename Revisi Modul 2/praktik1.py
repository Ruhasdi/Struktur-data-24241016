# impor library numpy 
import numpy as np # mengimpor library NumPy dan memberikan alias np untuk memudahkan penggunaan.
# NumPy adalah library yang digunakan untuk operasi numerik dan analisis data.

# membuat array dengan numpy
nilai_siswa = np.array([85, 55, 40, 90]) # membuat array NumPy yang berisi nilai-nilai siswa.
# np.array() adalah fungsi yang digunakan untuk membuat array NumPy dari list Python.
# list Python yang berisi nilai-nilai siswa.

# akses data pada array
print(nilai_siswa[3]) # mencetak nilai pada indeks ke-3 dari array nilai_siswa.
# indeks array dimulai dari 0, sehingga indeks ke-3 sebenarnya adalah nilai ke-4 dalam array.
# nilai pada indeks ke-3 adalah 90