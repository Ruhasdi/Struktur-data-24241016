# membuat array
arr = [1, 2, 3, 4, 5] # - Membuat array arr dengan 5 elemen: 1, 2, 3, 4, dan 5.

# Linear Traversal ke tiap elemen arr
print("Linear Traversal: ", end=" ") # Mencetak string "Linear Traversal: " dan tidak memulai baris baru setelah mencetak string tersebut (karena end=" ").
for i in arr: # Melakukan iterasi pada array arr untuk mengakses setiap elemen secara berurutan.
    print(i, end=" ") # Mencetak nilai elemen yang sedang diakses dalam iterasi, dan tidak memulai baris baru setelah mencetak nilai tersebut (karena end=" ").
# Elemen-elemen array akan dicetak secara berurutan, dipisahkan oleh spasi.

print()